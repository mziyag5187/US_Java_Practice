package b_29.group_meetings.week11_MixedTopics.practice_qs;

public class PrintOneToHundred {

    public static void main(String[] args) {

        int one = 'a'/'a'; //1
        String s1 = ".........."; // a string object comprised of 10 char

        for (int i = one; i <= (s1.length()*s1.length()) ; i++) {
            System.out.print(i + " ");
        }

    }
}
